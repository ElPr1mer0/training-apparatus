#include "resourse.h"
#include <QMediaPlayer>
#include <QDir>

////////////////////////////////////////////////////////////////////////
///////////////////////////SOUNDS::SOUNDS///////////////////////////////
////////////////////////////////////////////////////////////////////////
SOUNDS::SOUNDS(){
    sound = new QMediaPlayer;
}
////////////////////////////////////////////////////////////////////////
///////////////////////////SOUNDS::SOUNDS///////////////////////////////
////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////
///////////////////////////SOUNDS::~SOUNDS//////////////////////////////
////////////////////////////////////////////////////////////////////////
SOUNDS::~SOUNDS(){
    delete sound;
}
////////////////////////////////////////////////////////////////////////
///////////////////////////SOUNDS::~SOUNDS//////////////////////////////
////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////
///////////////////////////SOUNDS::Play/////////////////////////////////
////////////////////////////////////////////////////////////////////////
/// воспроизводит звук из получаемого файла.
void SOUNDS::Play(const QString &sound){
    this->sound->setMedia(QUrl(sound));
    this->sound->play();

}
