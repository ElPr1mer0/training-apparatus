#include "sounds.h"
#include <QMediaPlayer>
#include <QDir>


SOUNDS::SOUNDS(){
    sound = new QMediaPlayer;
}

SOUNDS::~SOUNDS(){
    delete sound;
}

////////////////////////////////////////////////////////////////////////
///////////////////////////////////PLAY/////////////////////////////////
////////////////////////////////////////////////////////////////////////
// Эта функция воспроизводит звук из получаемого файла.               //

void SOUNDS::Play(const QString &sound){
    this->sound->setMedia(QUrl(sound));
    this->sound->play();

}


